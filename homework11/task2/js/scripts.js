/* Якщо elem – довільний вузол DOM-елемента.

• Правда, що elem.lastChild.nextSibling завжди дорівнює null?

Так, правда. elem.lastChild завжди є останнім елементом, у нього немає nextSibling.

• Правда, що elem.children[0].previousSibling завжди дорівнює null?

Ні. elem.children[0] являє собою елемент, але перед ним можуть знаходитися інші типи вузлів, наприклад, текстовий чи коментар. */